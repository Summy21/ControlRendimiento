package com.example.summy.controlrendimiento.model;

public class Competencia {

    private int event_id;
    private String event_title;
    private String event_country;
    private String event_date;
    private String event_finish_date;
    private int event_region_id;
    private String event_region_name;

    public int getEvent_id() {
        return event_id;
    }

    public void setEvent_id(int event_id) {
        this.event_id = event_id;
    }

    public String getEvent_title() {
        return event_title;
    }

    public void setEvent_title(String event_title) {
        this.event_title = event_title;
    }

    public String getEvent_country() {
        return event_country;
    }

    public void setEvent_country(String event_country) {
        this.event_country = event_country;
    }

    public String getEvent_date() {
        return event_date;
    }

    public void setEvent_date(String event_date) {
        this.event_date = event_date;
    }

    public String getEvent_finish_date() {
        return event_finish_date;
    }

    public void setEvent_finish_date(String event_finish_date) {
        this.event_finish_date = event_finish_date;
    }

    public int getEvent_region_id() {
        return event_region_id;
    }

    public void setEvent_region_id(int event_region_id) {
        this.event_region_id = event_region_id;
    }

    public String getEvent_region_name() {
        return event_region_name;
    }

    public void setEvent_region_name(String event_region_name) {
        this.event_region_name = event_region_name;
    }


}
